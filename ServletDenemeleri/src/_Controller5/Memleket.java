package _Controller5;

public class Memleket {
	private int plaka;
	private String il;
	
	public Memleket(int plaka, String il) {
		super();
		this.plaka = plaka;
		this.il = il;
	}
	public int getPlaka() {
		return plaka;
	}
	public void setPlaka(int plaka) {
		this.plaka = plaka;
	}
	public String getIl() {
		return il;
	}
	public void setIl(String il) {
		this.il = il;
	}
	

}
